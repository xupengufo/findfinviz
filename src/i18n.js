export function translateReason(key) {
    return (reasonTranslations[state.activeLang] || reasonTranslations.en)[key] || key;
}

const languageChangeCallbacks = [];

export function onLanguageChange(callback) {
    languageChangeCallbacks.push(callback);
}

export function setLanguage(lang) {
    state.activeLang = lang;
    localStorage.setItem('lang', lang);
    document.documentElement.setAttribute('lang', lang);

    // Update static elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = translations[lang][key];
        if (text) {
            el.textContent = text;
        }
    });

    // Update Toggle button UI
    const langBtn = document.getElementById('lang-toggle');
    if (langBtn) {
        langBtn.textContent = lang === 'zh' ? '中' : 'EN';
    }

    // Trigger callbacks
    languageChangeCallbacks.forEach(callback => {
        try {
            callback(lang);
        } catch (e) {
            console.error('Error in language change callback:', e);
        }
    });
}
